export type NormKey = keyof typeof NORMS;

/** Как проверка выполняется — из PRD §5.2 */
export type Method = 'auto' | 'partial' | 'manual';

export const METHOD_LABEL: Record<Method, string> = {
  auto: 'Авто',
  partial: 'Частично',
  manual: 'Ручная',
};

export type Check = {
  /** Номер пункта чек-листа на лендинге, 1–10 */
  id: number;
  /** Формулировка с лендинга — как должно быть у соответствующего сайта */
  title: string;
  /**
   * Нейтральное имя предмета проверки — для отчёта.
   *
   * На лендинге пункты названы утверждениями: «Уведомление в РКН подано»,
   * «Нет логотипов запрещённых соцсетей». Это уместно в чек-листе «как надо»,
   * но в результате рядом с жёлтым «требует ручной проверки» такой заголовок
   * утверждает то, чего мы не знаем, а рядом с красным «нарушение» — прямо
   * противоречит вердикту. Поэтому в отчёте называем предмет, а факт сообщает
   * вердикт. Состав проверок при этом не меняется — только подпись.
   */
  subject?: string;
  /** Что именно проверяем — формулировка с лендинга */
  what: string;
  norms: NormKey[];
  method: Method;
  /** Почему пункт нельзя проверить автоматически (только для method: 'manual') */
  manualReason?: string;
};

/** Имя пункта для отчёта: нейтральное, если формулировка с лендинга — утверждение. */
export function subjectOf(check: Check): string {
  return check.subject ?? check.title;
}

