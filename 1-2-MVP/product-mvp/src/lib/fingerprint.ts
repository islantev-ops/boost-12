import * as cheerio from 'cheerio';

/** Больше этого не разбираем: каркас виден задолго до конца документа, а
 *  разбор мегабайтной страницы стоит секунду. Обрезка — единственное, что
 *  реально ограничивает стоимость: селектор всё равно проходит документ. */
const MAX_HTML = 300_000;

/** Сколько самых частых элементов каркаса образуют отпечаток. */
const TOP_KEYS = 15;

/**
 * Отпечаток каркаса страницы: какие элементы вёрстки на ней преобладают.
 *
 * Нужен, чтобы не качать 3000 одинаковых карточек товара: страницы с одним
 * отпечатком считаются однотипными, и с каждой берётся несколько
 * представителей. Текст игнорируется — он у карточек разный, а каркас один.
 *
 * Считаем ПО СОСТАВУ, а не по порядку, и только по основному содержимому:
 * - по порядку нельзя, потому что один необязательный блок («Скидка»)
 *   сдвигает весь список и рвёт совпадение однотипных карточек;
 * - меню и подвал исключены, иначе на сайте с большим меню все страницы
 *   выглядят одинаково, и мы пропустим те, что надо было проверить.
 */
export function templateFingerprint(html: string): string {
  const $ = cheerio.load((html ?? '').slice(0, MAX_HTML));
  $('script, style, noscript, svg').remove();

  // Основное содержимое: главный блок, а если разметка его не выделяет —
  // body без сквозных элементов сайта.
  let scope = $('main, article, [role=main]').first();
  if (scope.length === 0) {
    $('header, footer, nav, aside').remove();
    scope = $('body');
  }

  const counts = new Map<string, number>();
  scope.find('*').each((_, el) => {
    const tag = (el as { tagName?: string }).tagName ?? '';
    const cls = ($(el).attr('class') ?? '').trim().split(/\s+/)[0] ?? '';
    const key = cls ? `${tag}.${cls}` : tag;
    counts.set(key, (counts.get(key) ?? 0) + 1);
  });

  // Самые частые элементы каркаса. Сортировка по количеству, затем по имени —
  // чтобы отпечаток был одинаковым при равных частотах.
  const top = [...counts.entries()]
    .sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]))
    .slice(0, TOP_KEYS)
    .map(([key]) => key)
    .sort();

  return hash(top.join('|'));
}

/** djb2 — короткий стабильный хеш, криптостойкость здесь не нужна. */
function hash(s: string): string {
  let h = 5381;
  for (let i = 0; i < s.length; i++) h = ((h << 5) + h + s.charCodeAt(i)) | 0;
  return (h >>> 0).toString(36);
}
