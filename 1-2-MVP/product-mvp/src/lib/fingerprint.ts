import * as cheerio from 'cheerio';

/** Больше этого не разбираем: каркас виден задолго до конца документа, а
 *  разбор мегабайтной страницы стоит секунду. Сама по себе обрезка по длине
 *  строки НЕ ограничивает стоимость — см. MAX_TAGS ниже. */
const MAX_HTML = 300_000;

/** Ограничение по числу символов не ограничивает стоимость разбора: битая
 *  или экстремально вложенная разметка укладывается в MAX_HTML символов, а
 *  построение дерева в парсере на таких входах растёт квадратично. Замер
 *  (ревью 2026-07-21, воспроизведён при правке): 100 000 незакрытых `<b>`
 *  (= ровно предел MAX_HTML) разбираются 16–19 секунд; вложенность `<div>`
 *  глубиной 20 000 — 6.8 секунды. Обход вызывает эту функцию на каждой из
 *  300 страниц при бюджете 20 минут — одна битая страница на старой CMS
 *  съедает заметную часть бюджета.
 *
 *  Лечится ограничением не длины строки, а числа тегов: сканируем сырую
 *  строку (это линейно и дёшево, парсер её не строит) и обрезаем на позиции
 *  N-го символа `<`, в дополнение к MAX_HTML — какое ограничение наступит
 *  раньше. Значение подобрано замером на худшем из проверенных случаев
 *  (глубокая вложенность `<div>`, эта машина, среднее по 5 прогонам):
 *  4000 тегов ≈ 230мс, 6000 ≈ 500–650мс, 8000 ≈ 800мс. Взят запас: 4000. */
const MAX_TAGS = 4000;

/** Обрезает html на позиции символа `<`, с которого начинается
 *  (maxTags + 1)-й тег, — то есть оставляет не больше maxTags тегов.
 *  Если тегов меньше или столько же, строка возвращается как есть. */
function truncateByTagCount(html: string, maxTags: number): string {
  let count = 0;
  for (let i = 0; i < html.length; i++) {
    if (html.charCodeAt(i) === 60 /* '<' */) {
      count++;
      if (count > maxTags) return html.slice(0, i);
    }
  }
  return html;
}

/**
 * Отпечаток каркаса страницы: набор элементов вёрстки, из которых она сложена.
 *
 * Нужен, чтобы не качать 3000 одинаковых карточек товара: страницы с одним
 * отпечатком считаются однотипными, и с каждой группы берётся несколько
 * представителей. Текст игнорируется — он у карточек разный, а каркас один.
 *
 * Правило «схлопывания редких»: если пара (тег, первый класс) встретилась на
 * странице 2 и более раз — это часть каркаса, кладём `tag.class`. Если один
 * раз — это может быть необязательное украшение, кладём только `tag`.
 *
 * Правило помогает, когда редкий элемент оформлен тегом, который на странице
 * и так уже есть: например, единичный `span.discount-badge` схлопывается в
 * `span`, и бейдж «Скидка» не рвёт совпадение однотипных карточек. Если же
 * редкий элемент оформлен тегом, которого больше нигде на странице нет
 * (например `<mark class="discount-badge">`), совпадение всё равно
 * ломается — это не гарантия правила, а его свойство на конкретной
 * разметке. Ошибка идёт в безопасную сторону: однотипные страницы разъедутся
 * по отпечатку, и обход скачает лишние экземпляры вместо того, чтобы
 * пропустить нужные. При этом по-настоящему повторяющиеся элементы
 * (`li.menu__item` × 450) класс сохраняют и продолжают различать типы
 * страниц.
 *
 * Отпечаток — МНОЖЕСТВО ключей: ни порядок, ни счётчики не учитываются, иначе
 * один лишний блок сдвигает всё и ломает группировку.
 */
export function templateFingerprint(html: string): string {
  const bounded = truncateByTagCount((html ?? '').slice(0, MAX_HTML), MAX_TAGS);
  const $ = cheerio.load(bounded);
  $('script, style, noscript, svg').remove();

  const raw = new Map<string, { tag: string; cls: string; count: number }>();
  $('*').each((_, el) => {
    const tag = (el as { tagName?: string }).tagName ?? '';
    const cls = ($(el).attr('class') ?? '').trim().split(/\s+/)[0] ?? '';
    const key = `${tag} ${cls}`;
    const entry = raw.get(key);
    if (entry) entry.count++;
    else raw.set(key, { tag, cls, count: 1 });
  });

  const keys = new Set<string>();
  for (const { tag, cls, count } of raw.values()) {
    keys.add(count >= 2 && cls ? `${tag}.${cls}` : tag);
  }

  return hash([...keys].sort().join('|'));
}

/** djb2 — короткий стабильный хеш, криптостойкость здесь не нужна. */
function hash(s: string): string {
  let h = 5381;
  for (let i = 0; i < s.length; i++) h = ((h << 5) + h + s.charCodeAt(i)) | 0;
  return (h >>> 0).toString(36);
}
