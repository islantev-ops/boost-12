import type { Method, NormKey } from './legal';

/**
 * Ровно три исхода проверки — PRD §8: «"тихого" четвёртого исхода
 * (пропало без следа) быть не должно».
 *
 *  violation — нарушение подтверждено всеми факторами, есть пруф
 *  ok        — соответствует
 *  manual    — однозначно определить нельзя; не заявляем и НЕ отбрасываем
 */
export type Verdict = 'violation' | 'ok' | 'manual';

/**
 * Один фактор перепроверки (PRD §5.3). Нарушение заявляется, только когда
 * ВСЕ факторы сошлись. Любой `unknown` роняет проверку в `manual`.
 */
export type Factor = {
  name: string;
  /** Голос фактора: за нарушение / за соответствие / не смог определить */
  vote: 'violation' | 'ok' | 'unknown';
  detail: string;
  /** Дословный фрагмент кода сайта — доказательство, которое можно перепроверить */
  evidence?: Evidence;
};

/** Пруф: реальный кусок HTML с адресом страницы, где он найден */
export type Evidence = {
  url: string;
  /** Читаемый вид: пробелы схлопнуты, длина обрезана */
  snippet: string;
  /** Номер строки в исходнике страницы, если удалось определить */
  line?: number;
  /**
   * Дословный кусок исходника — ровно те байты, что отдаёт сайт.
   *
   * Нужен, чтобы пруф можно было найти на странице поиском. Читаемый `snippet`
   * для этого не годится: в нём схлопнуты пробелы, а парсер ещё и переписывает
   * разметку по-своему (`async` → `async=""`), и поиск такой строки на сайте
   * ничего не находит.
   */
  exact?: string;
};

export type Finding = {
  checkId: number;
  title: string;
  what: string;
  verdict: Verdict;
  method: Method;
  norms: NormKey[];
  /** Человеческая формулировка вывода — идёт в отчёт и письмо */
  summary: string;
  factors: Factor[];
  evidence: Evidence[];
  /**
   * Документ, на который опирается вывод: найденная Политика, оферта, согласие.
   *
   * Скриншотов у нас нет, поэтому вывод «соответствует» обязан вести на то,
   * что мы прочитали, — иначе это утверждение без доказательства. Работает
   * в обе стороны: «в Политике рассылка не описана» тоже нужно уметь открыть
   * и проверить.
   */
  doc?: DocRef;
  /** Насколько сильное нарушение — для отбора топ-3 в письмо */
  severity: number;
};

/** Ссылка на документ сайта, который мы смотрели. */
export type DocRef = {
  /** Абсолютный адрес — по нему можно открыть и убедиться */
  url: string;
  /** Что это за документ: «Политика конфиденциальности» и т.п. */
  label: string;
};

export type CrawledPage = {
  url: string;
  status: number;
  html: string;
  /** Текст страницы без тегов — для поиска англицизмов и смысловых проверок */
  text: string;
};

/**
 * Где физически стоит сайт. Факт, а не вывод: это ровно то, что вернули
 * источники, без домыслов о базе данных.
 *
 * `country` берётся из RDAP и есть НЕ только у RIPE-региона — отозванная
 * посылка (MINOR 2 из финального ревью): проверено на живых адресах, APNIC
 * (103.21.244.1 → US) и AFRINIC (197.234.240.1 → ZA) страну тоже отдают.
 * Поля нет только у ARIN. Поэтому `null` здесь значит «реестр страну не
 * назвал» (в т.ч. прислал не двухбуквенный код, см. geo.ts), а не «регион не
 * RIPE» и не «страны нет физически».
 */
export type HostingFact = {
  /** Проверенные адреса (первые MAX_IPS_CHECKED из A-записей домена) — не все A-записи домена */
  ips: string[];
  /**
   * Адрес из `ips`, к которому реально относятся `country`/`netname`/`geoCountry`
   * ниже, когда представителем для проверки выбран один конкретный адрес, а
   * не весь набор `ips` (см. resolveHosting в geo.ts). Не задано или `null` —
   * либо country/netname не определены, либо (ветка «все проверенные адреса
   * подтверждены как RU») они относятся ко всем `ips` разом.
   *
   * Нужен, чтобы в отчёте не печатать как измеренные адреса, по которым
   * страну на самом деле не узнавали (IMPORTANT 1 из финального ревью:
   * раньше в текст обвинения попадали все проверенные IP, а страна/сеть были
   * известны только по одному из них).
   */
  ip?: string | null;
  /** ISO-код страны из RDAP; null — реестр страну не сообщил или прислал не двухбуквенный код */
  country: string | null;
  /** Имя сети из RDAP: REGRU-NETWORK, CLOUDFLARENET */
  netname: string | null;
  /** Страна по данным ipwho.is; null — не спрашивали или не ответил */
  geoCountry: string | null;
  /** netname опознан как CDN — за ним происхождение не видно */
  isCdn: boolean;
  /** Какие источники реально ответили: 'rdap', 'ipwho.is' */
  confirmedBy: string[];
  /** Человекочитаемая причина, почему факт неполон */
  error?: string;
};

/**
 * Сколько сайта мы реально посмотрели. Нужен, чтобы отчёт не выдавал
 * «документа нет» после обхода трети сайта: вывод об отсутствии допустим
 * только при полном обходе.
 */
export type CrawlCoverage = {
  /** Сколько страниц скачали */
  crawled: number;
  /** Сколько внутренних адресов вообще нашли на сайте */
  discovered: number;
  /** Пропущено как однотипные (лимит на один отпечаток шаблона) */
  skippedByTemplate: number;
  /** Пропущено из-за потолка страниц или лимита времени */
  skippedByLimit: number;
  /** Обход закончился сам, а не упёрся в бюджет */
  complete: boolean;
  /** Почему обход остановился */
  stopReason: 'done' | 'pageLimit' | 'timeLimit';
};

export type SiteSnapshot = {
  inputUrl: string;
  finalUrl: string;
  reachable: boolean;
  error?: string;
  /** Определённая CMS: 'bitrix' | 'wordpress' | ... | null */
  cms: string | null;
  /**
   * Похоже, что контент рисуется на клиенте (SPA). Тогда проверки «чего-то нет»
   * недостоверны — они уходят в `manual`, а не выдают ложное нарушение.
   */
  clientRendered: boolean;
  /**
   * Виден ли подвал в серверном HTML. Ссылки на документы живут в подвале;
   * если его дорисовывает скрипт, «ссылки нет» означает лишь «мы не смотрели».
   */
  footerVisible: boolean;
  /**
   * Сайт закрыт антибот-защитой (Cloudflare, DDoS-Guard, KillBot): даже
   * реальный браузер не прошёл челлендж, вместо сайта отдаётся заглушка.
   * Тогда автопроверка невозможна — проверки не запускаются, письмо не
   * генерируется (см. auditSite). Иначе в коде заглушки нашлись бы чужие
   * счётчики и ушли бы клиенту как его нарушение.
   */
  blockedByAntibot: boolean;
  /** Факты охвата: сколько сайта мы посмотрели. См. canProveAbsence в checks.ts */
  coverage: CrawlCoverage;
  /**
   * Где стоит сайт. Заполняется в `auditSite`, не в `crawlSite`: краул знает
   * про страницы, гео — про сеть, и мешать их не нужно. `null` — не смотрели.
   */
  hosting: HostingFact | null;
  pages: CrawledPage[];
};

export type AuditResult = {
  snapshot: SiteSnapshot;
  findings: Finding[];
  anglicisms: AnglicismHit[];
};

export type AnglicismHit = {
  word: string;
  suggestion: string;
  url: string;
  context: string;
};
